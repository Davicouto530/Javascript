"use strict"

/**
 * Maior -> >
 * Maior ou igual -> >=
 * Menor -> <
 * Menor ou igual -> <=
 * Igual -> ==
 * Not -> !
 * Diferente -> !=
 */

let num1 = 10
let num2 = 5
let num3 = 10

console.log(num1 > num2)//"num1" é maior que "num2"

console.log(num1 < num2)//"num1" é menor que "num2"

console.log(num1 >= num3)//"num1" é maior ou igual que "num3"

console.log(num1 == num3)//"num1" é igual que "num3"

console.log(!(num1 == num3))
//Operador de not "!", ele inverte o valor, se for verdadeiro, inverte para falso, mesma coisa se for falso, inverte para verdadeiro

console.log(num1 != num3)
//Operador de diferente, "num1" diferente de "num3", vai da falso, porque são iguais

