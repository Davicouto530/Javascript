"use strict"

/**
 * Pós e Pré incremento
 */

let n = 10

n++ //Pós incremento

console.log(n)

console.log(++n)

n += 10 //Atribuindo e somando com o valor de "n"

//O "-"(menos) colocado em uma variável, inverte o valor dela, se a variável tiver um número positivo e colocar o sinal de "-", vira um número negativo. E se tiver um número negativo na primeira e colocar o "-", o número vira positivo.

let x = -n //"n" era 10 positivo, e agora "n" virou 10 negativo "-10"

console.log(x)

